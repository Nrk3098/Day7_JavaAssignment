//Program2: To display the default value of all primitive data types
package com.basicjava;

public class Datatype {

            static int num1;
            static float num2;
            static double num3;
            static boolean num4;
            static long num5;
            static String num6;

            public static void main(String[] args) {
                System.out.println("Default value of integer: " + num1);

                System.out.println("Default value of float: " + num2);

                System.out.println("Default value of double: " + num3);

                System.out.println("Default value of boolean: " + num4);

                System.out.println("Default value of long: " + num5);

                System.out.println("Default value of String: " + num6);
            }

        }


